class Num4 {
    public static void main(String[] args) {

        int i = 1;
        while (i <= 3) {
            int count = 1;
            int r = 3;
            while (r >= 1) {
                System.out.print(count);
                count++;
                r--;
            }

            System.out.println();
            i++;
        }
    }
}
